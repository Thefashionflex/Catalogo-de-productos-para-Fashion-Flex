const LOCAL_STORAGE_CATALOG_KEY = 'fashionFlexLiveCatalog';

export const loadCatalogFromLocalStorage = (): CatalogCategory[] => {
  try {
    const storedCatalog = localStorage.getItem(LOCAL_STORAGE_CATALOG_KEY);
    if (storedCatalog) {
      const parsed = JSON.parse(storedCatalog);
      // Basic validation of stored data structure
      if (Array.isArray(parsed) && parsed.every(cat => cat.id && cat.name && Array.isArray(cat.items))) {
         console.log("Loaded catalog from localStorage");
        return parsed;
      } else {
        console.warn("Invalid catalog data in localStorage, using initial data from constants.ts.");
        localStorage.removeItem(LOCAL_STORAGE_CATALOG_KEY); 
      }
    }
  } catch (error) { 
    console.error("Error loading catalog from localStorage:", error);
  }
  console.log("Initializing with default catalog data from constants.ts (new extensive list).");
  return JSON.parse(JSON.stringify(INITIAL_CATALOG_DATA)); // Return a deep copy
};

export const saveCatalogToLocalStorage = (catalog: CatalogCategory[]): void => {
  try {
    localStorage.setItem(LOCAL_STORAGE_CATALOG_KEY, JSON.stringify(catalog));
  } catch (error) { 
    console.error("Error saving catalog to localStorage:", error);
  }
};
