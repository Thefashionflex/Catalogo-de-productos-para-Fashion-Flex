const allNewProducts: Omit<ProductItem, 'id' | 'categoryName' | 'categoryId'>[] = [
    ...parsedNewImageProducts,
    ...existingProductDefinitions
];


// Helper to create full ProductItem from the parsed data
const createFullProductItem = (
    itemData: Omit<ProductItem, 'id' | 'categoryName' | 'categoryId'>,
    categoryId: 'perfumes' | 'calzado' | 'ropa' | 'accesorios',
    categoryName: string
): ProductItem => {
    return {
        ...itemData,
        id: itemData.sku, // Use SKU as ID
        categoryId,
        categoryName,
        brand: itemData.brand || extractBrandFromName(itemData.name) || '',
    };
};

const perfumeItems: ProductItem[] = allNewProducts
    .filter(p => {
        const nameLower = p.name.toLowerCase();
        return (
            nameLower.includes('edp') || 
            nameLower.includes('edt') || 
            nameLower.includes('parfum') ||
            nameLower.includes(' ml') || 
            (nameLower.includes('set') && nameLower.includes(' ml'))
        );
    })
    .map(p => createFullProductItem(p, 'perfumes', 'Perfumes'));

const calzadoItems: ProductItem[] = allNewProducts
    .filter(p => {
        const nameLower = p.name.toLowerCase();
        return (nameLower.includes('tenis') || nameLower.includes('sandalia') || nameLower.includes('zapatilla') || nameLower.includes('zapato') || nameLower.includes('suede') || nameLower.includes('court') || nameLower.includes('runner') || nameLower.includes('charged') || nameLower.includes('boots') || nameLower.includes(' mx')  || nameLower.includes(' xm')) &&
               !perfumeItems.some(perf => perf.sku === p.sku); 
    })
    .map(p => createFullProductItem(p, 'calzado', 'Calzado'));

const ropaItems: ProductItem[] = allNewProducts
    .filter(p => {
        const nameLower = p.name.toLowerCase();
        return (nameLower.includes('playera') || nameLower.includes('camiseta') || nameLower.includes('sudadera') || nameLower.includes('leggings') || nameLower.includes('conjunto deportivo') || nameLower.includes('talla ') || nameLower.includes('tank')) &&
               !perfumeItems.some(perf => perf.sku === p.sku) &&
               !calzadoItems.some(cal => cal.sku === p.sku);
    })
    .map(p => createFullProductItem(p, 'ropa', 'Ropa'));

const accesorioItems: ProductItem[] = allNewProducts
    .filter(p => {
         const nameLower = p.name.toLowerCase();
        return (nameLower.includes('maleta') || nameLower.includes('guante') || nameLower.includes('cuerda') || nameLower.includes('mochila') || nameLower.includes('bolsa') || nameLower.includes('monedero') || nameLower.includes('lentes') || nameLower.includes('gorra') || nameLower.includes('cosmetiquera') || nameLower.includes('cartera') || nameLower.includes('reloj')) && 
               !perfumeItems.some(perf => perf.sku === p.sku) &&
               !calzadoItems.some(cal => cal.sku === p.sku) &&
               !ropaItems.some(rop => rop.sku === p.sku);
    })
    .map(p => createFullProductItem(p, 'accesorios', 'Accesorios'));


// Ensure all products are categorized. If any are left, put them in a default category or log.
const categorizedSKUs = new Set([
    ...perfumeItems.map(p => p.sku),
    ...calzadoItems.map(p => p.sku),
    ...ropaItems.map(p => p.sku),
    ...accesorioItems.map(p => p.sku),
]);

const uncategorizedProducts = allNewProducts.filter(p => !categorizedSKUs.has(p.sku));
if (uncategorizedProducts.length > 0) {
    console.warn("Uncategorized products found, adding to 'Accesorios' by default:", uncategorizedProducts.map(p => ({name: p.name, sku: p.sku })));
    uncategorizedProducts.forEach(p => {
        // Double check it's not already in accesorioItems to prevent duplicates if logic above missed it
        if (!accesorioItems.some(existingAcc => existingAcc.sku === p.sku)) {
            accesorioItems.push(createFullProductItem(p, 'accesorios', 'Accesorios'));
        }
    });
}


