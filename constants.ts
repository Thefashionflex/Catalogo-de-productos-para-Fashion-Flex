// Updated to handle D/M/YYYY and DD/MM/YYYY, H:MM and HH:MM
const parseDateTimeToISO = (dateString: string, timeString: string): string => {
  const dateParts = dateString.split('/');
  const timeParts = timeString.split(':');

  const day = parseInt(dateParts[0].padStart(2, '0'));
  const month = parseInt(dateParts[1].padStart(2, '0')) - 1; // Months in JS are 0-indexed
  let year = parseInt(dateParts[2]);
  if (year < 2000) year += 2000; 

  const hour = parseInt(timeParts[0].padStart(2, '0'));
  const minute = parseInt(timeParts[1].padStart(2, '0'));
  
  try {
    const d = new Date(Date.UTC(year, month, day, hour, minute));
    if (isNaN(d.getTime())) {
        console.error(`Invalid date created for: ${dateString} ${timeString}. Using current date as fallback.`);
        return new Date().toISOString(); 
    }
    return d.toISOString();
  } catch (e) {
    console.error(`Error parsing date: ${dateString} ${timeString}`, e);
    return new Date().toISOString(); 
  }
};

