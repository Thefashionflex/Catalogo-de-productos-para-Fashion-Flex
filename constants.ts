const extractBrandFromName = (productName: string): string => {
    const nameLower = productName.toLowerCase();
    // Check for specific multi-word brands or abbreviations first
    if (nameLower.includes("tommy hilfiger") || nameLower.includes("tommy")) return "Tommy Hilfiger";
    if (nameLower.includes("calvin klein") || nameLower.includes("ck ")) return "Calvin Klein"; // CK followed by space
    if (nameLower.includes("nine west")) return "Nine West";
    if (nameLower.includes("true religion")) return "True Religion";
    if (nameLower.includes("jessica simpson")) return "Jessica Simpson";
    if (nameLower.includes("steve madden")) return "Steve Madden";
    if (nameLower.includes("juicy couture") || nameLower.includes("juicy ")) return "Juicy Couture";


    for (const brand of BRANDS_LIST) {
        if (nameLower.includes(brand.toLowerCase())) {
            // Handle specific cases like "Playera Original Penguin"
            if (brand === "Playera Original Penguin") return "Original Penguin";
            return brand;
        }
    }
    return '';
};

const calculateCostPrice = (priceString: string): number => {
    const numericPrice = parseFloat(priceString.replace('$', '').replace(',', ''));
    if (isNaN(numericPrice)) return 0;
    return parseFloat((numericPrice * 0.6).toFixed(2));
};

const formatSellingPrice = (priceInput: string): string => {
    const numericPrice = parseFloat(priceInput.replace('$', '').replace(',', ''));
    if (isNaN(numericPrice)) return '$0.00';
    return `$${numericPrice.toFixed(2)}`;
};

const parseNewProductCostPrice = (costInput: string): number => {
    const numericCost = parseFloat(costInput.replace('$', '').replace(',', ''));
    return isNaN(numericCost) ? 0 : numericCost;
}

const extractSizeFromName = (name: string): string[] | undefined => {
    const sizeMatch = name.match(/TALLA\s+([\d.]+)\s*MX/i);
    if (sizeMatch && sizeMatch[1]) {
        return [`${sizeMatch[1]} MX`];
    }
    return undefined;
};


